const fs = require('node:fs');
const path = require('node:path');

const root = path.resolve(__dirname, '..');
const source = path.join(root, 'assets', 'licenses.txt');
const output = path.join(root, 'assets', 'licenses-public.txt');
const tsOutput = path.join(root, 'app', 'licenses-content.ts');

if (!fs.existsSync(source)) {
  console.error('licenses.txt が見つかりません。先に npm run licenses:txt を実行してください。');
  process.exit(1);
}

const raw = fs.readFileSync(source, 'utf8');
const lines = raw.split(/\r?\n/);

const filtered = lines.filter((line) => {
  const trimmed = line.trimStart();
  return !/^[├└│\s─]+(path|email|publisher|licenseFile|private|noticeFile):/u.test(trimmed);
});

const header = [
  'attendance-reminder - Open Source Licenses',
  `Generated at: ${new Date().toISOString()}`,
  '',
  'This list includes production dependencies used by this app.',
  '',
].join('\n');

const content = `${header}${filtered.join('\n')}\n`;

// Write public text file
fs.writeFileSync(output, content, 'utf8');
console.log(`公開用ライセンス一覧を出力しました: ${output}`);

// Write TypeScript constant file for app import
const tsContent = `// Auto-generated by format-licenses.js
export const LICENSES_CONTENT = ${JSON.stringify(content)};
`;
fs.writeFileSync(tsOutput, tsContent, 'utf8');
console.log(`TypeScript ライセンス定数を出力しました: ${tsOutput}`);
